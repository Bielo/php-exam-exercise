                <h3>Krem dyniowo - marchewkowy z pistacjami</h3>
                <p class="opis">Składniki:</p>
                <ul>
                <li>10 - 15 młodych marchewek</li>
                <li>750 g pokrojonej w kostkę dyni</li>
                <li>500 ml bulionu warzywnego</li>
                <li>20 g pistacji</li>
                <li>100 g topionego serka śmietankowego</li>
                <li>sól i pieprz</li>
                <li>ząbek czosnku</li>
                <li>zioła</li>
                </ul>
