                <h3>Marchewkowo-kokosowe tiramisu bez jajek</h3>
                <p class="opis">Składniki:</p>
                <ul>
                <li>500 g serka mascarpone</li>
                <li>150 ml śmietany kremówki</li>
                <li>50 g cukru pudru</li>
                <li>120 - 150 g okrągłych biszkoptów</li>
                <li>sok wyciśnięty z 4 marchewek i 3 drobno starte marchewki</li>
                <li>0.5 cytryny</li>
                <li>2 łyżki likieru kokosowego/nalewki kokosowej</li>
                <li>2 łyżki wiórków kokosowych</li>
                </ul>
                <p class="opis">Czas przygotowania:</p><p>do 30 min</p>
                <p class="opis">Sposób przygotowania:</p>
                <p class="przygotowanie">Zaczynamy od podłączenia do prądu sokowirówki - z 4 marchewek i cytryny przygotowujemy sok do nasączania biszkoptów. Dolewamy likier kokosowy, odstawiamy na chwilę. Na drobnej tartce ścieramy marchewki do kremu, odsączamy dokładnie, odstawiamy na chwilę. Ubijamy na sztywno kremówkę (uprzednio warto wstawić misę miksera oraz łopatki do zamrażarki). Tworzymy krem: w dużej misce umieszczamy serek mascarpone, delikatnie mieszamy łyżką, raz po raz dodając kremówkę oraz cukier puder. Do połączonego kremu dodajemy wiórki marchewki i wiórki kokosowe. Biszkopty krótko nasączamy w soku, układamy pierwszą warstwę w pucharkach (razie potrzeby łamiemy biszkopty tak, by dokładnie wypełniały dno naczynia), przekładamy połową kremu, układamy drugą warstwę biszkoptów i drugą warstwę kremu. Odstawić do lodówki na co najmniej godzinę. Podajemy udekorowane wiórkami czekolady, a podczas delektowania zastanawiamy się nad kolejnymi wariacjami nt. tiramisu.</p>
