                <h3>Verrines z wędzonym łososiem, twarożkiem i kiełkami stir-fry</h3>
                <p class="opis">Składniki:</p>
                <ul>
                <li>4 plasteki filetu z wędzonego łososia</li>
                <li>50g kiełków stir-fry</li>
                <li>4 łyżki chudego twarożku</li>
                <li>łyżeczka midowej musztardy diżońskiej</li>
                <li>szczypta posiekanego koperku/szczypiorku</li>
                <li>świeżo zmielone sól i pieprz</li>
                <li>kilka kropel oliwy z oliwek</li>
                </ul>
                <p class="opis">Czas przygotowania:</p><p>do 30 min</p>
                <p class="opis">Sposób przygotowania:</p>
                <p class="przygotowanie">Oliwę należy rozgrzać na patelni, a następnie smażyć na niej kiełki przez ok. 3 minuty, cały czas mieszając. Rozchodzący się apetyczny aromat będzie oznaką, iż są już gotowe. Przekładamy je do miseczki i odstawiamy na chwilkę do ostygnięcia. Za pomocą blendera lub łyżki łączymy twarożek z odrobiną soli, pieprzu, szczyptą koperku oraz musztardą. Łososia skrapiamy odrobiną soku z cytryny (by ładnie nam kontrastował ze słodyczą musztardy) i zwijamy w fantazyjne różyczki. Teraz najważniejszy etap - układanie. W przygotowanych niewielkich szklaneczkach bądź kieliszkach (np. do wina, jak w naszym przypadku) delikatnie układamy warstwami: łyżkę kiełków, łyżkę twarożku i różyczkę z łososia. Warstwy pięknie kontrastują ze sobą. Przekąska prezentuje się obłędnie i może czekać w lodówce na gości.</p>
