                <p class="opis">Sposób przygotowania:</p>
                <p class="przygotowanie">Zagotowujemy bulion. Lubimy gęste kremy, stąd taka ilość bulionu - jeśli preferujecie inną konsystencję , zwiększcie jego ilość. Marchewki myjemy, szorujemy szczoteczką i kroimy w dowolnej wielkości części. Umieszczamy je w gotującym się bulionie wraz z listkiem laurowym i ząbkiem czosnku. Gotujemy pod przykryciem ok. 5 min., po czym dodajemy pokrojoną w kostkę dynię i gotujemy dalej. Gdy warzywa zmiękną, zdejmujemy garnek z ognia, wyjmujemy listek laurowy i za pomocą blendera miksujemy zupę na krem. By zapewnić aksamitność, dodajemy serek, następnie przyprawiamy szczyptą soli, pieprzu, tymianku i papryki. Słodycz warzyw przełamujemy słonymi pistacjami. Obieramy większość (kilka pozostawiamy do dekoracji) i przy pomocy moździerza kruszymy na kawałeczki. Dodajemy do pistacje do kremu, który ponownie umieszczamy na gazie, zagotowujemy, upewniając się, że serek odpowiednio się rozpuścił. Do zupy doskonale pasują płatki migdałów - jeśli macie je w pobliżu, warto je uprażyć na suchej, rozgrzanej patelni i dodać do kremu - chrupanie podczas degustacji jest dozwolone :) Krem podajemy natychmiast, udekorowany kilkoma pistacjami.</p>
